-- Создание таблиц

CREATE TABLE fuel_types (
    id SERIAL PRIMARY KEY,
    name VARCHAR(100) NOT NULL,
    code_1c VARCHAR(6) NOT NULL,
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    unit VARCHAR(10) NOT NULL DEFAULT 'л'
);

CREATE TABLE stations (
    id SERIAL PRIMARY KEY,
    name VARCHAR(255) NOT NULL,
    code_1c VARCHAR(50) NOT NULL,
    address TEXT NOT NULL,
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);

CREATE TABLE clients (
    id SERIAL PRIMARY KEY,
    inn VARCHAR(255) NOT NULL DEFAULT NULL,
    name VARCHAR(255) NOT NULL,
    address TEXT NULL,
    phone VARCHAR(20) NULL,
    email VARCHAR(100) NULL,
    login VARCHAR(255) NOT NULL DEFAULT NULL,
    password VARCHAR(255) NOT NULL,
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    admin BOOLEAN NOT NULL DEFAULT false,
    operator BOOLEAN NOT NULL DEFAULT false
);

CREATE TABLE fuel_cards (
    id SERIAL PRIMARY KEY,
    client_id INTEGER NOT NULL REFERENCES clients(id),
    fuel_type_id INTEGER NOT NULL REFERENCES fuel_types(id),
    balance_liters NUMERIC(10,2) DEFAULT 0,
    card_code VARCHAR(4) NOT NULL,
    pin_code VARCHAR(4) NOT NULL,
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    status VARCHAR(20) NOT NULL DEFAULT 'активна',
    block_reason TEXT NULL,
    daily_limit NUMERIC(10,3) NOT NULL DEFAULT 0,
    card_index INTEGER NOT NULL DEFAULT 0
);

CREATE TABLE card_operations (
    id SERIAL PRIMARY KEY,
    fuel_card_id INTEGER NOT NULL REFERENCES fuel_cards(id),
    operation_date TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    operation_type VARCHAR(50) NOT NULL,
    quantity NUMERIC(10,2) NOT NULL,
    price NUMERIC(10,2) NULL,
    amount NUMERIC(10,2) NULL,
    comment VARCHAR(100) NULL,
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    station_id INTEGER NOT NULL REFERENCES stations(id)
);

